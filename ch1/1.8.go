// Fetch prints the content found at each specified URL.
/*
io.copy(dst, src) reads from src and copies to dst
use it instead of ioutil.ReadAll to copy the response body to
os.stdout without requiring a buffer large enough to hold the entire
stream. Check the error result of io.copy
*/

package main

import (
	"fmt"
	"io"
	"net/http"
	"os"
	"strings"
)

func main() {
	for _, url := range os.Args[1:] {
		// prefix http if string doesn't start with http
		if !strings.HasPrefix(url, "http://") {
			url = "http://" + url
		}

		resp, err := http.Get(url)
		if err != nil {
			fmt.Fprintf(os.Stderr, "fetch: %v\n", err)
			os.Exit(1)
		}
		written, err := io.Copy(os.Stdout, resp.Body)
		resp.Body.Close()
		if err != nil {
			fmt.Fprintf(os.Stderr, "fetch: reading %s: %v\n", url, err)
			os.Exit(1)
		}
		fmt.Printf("%d bytes copied", written)
	}
}

